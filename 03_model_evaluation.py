def evaluate_model(model, X_test, y_test, model_name):
    """
    Computes comprehensive metrics for credit risk evaluation.
    Returns: Dictionary of metrics, Confusion Matrix, ROC Curve values
    """
    # Predictions
    y_pred = model.predict(X_test)

    # Probabilities (handle pipelines correctly)
    if hasattr(model, 'predict_proba'):
        y_pred_proba = model.predict_proba(X_test)[:, 1]
    else:
        y_pred_proba = None

    # Metrics
    metrics = {
        'Model': model_name,
        'Accuracy': accuracy_score(y_test, y_pred),
        'Precision': precision_score(y_test, y_pred, pos_label=1, zero_division=0),
        'Recall': recall_score(y_test, y_pred, pos_label=1, zero_division=0),  # PRIMARY METRIC
        'F1-Score': f1_score(y_test, y_pred, pos_label=1, zero_division=0),
        'ROC-AUC': roc_auc_score(y_test, y_pred_proba) if y_pred_proba is not None else 0.0
    }

    # Confusion Matrix
    cm = confusion_matrix(y_test, y_pred)

    # ROC Curve Values
    if y_pred_proba is not None:
        fpr, tpr, thresholds = roc_curve(y_test, y_pred_proba)
    else:
        fpr, tpr, thresholds = None, None, None

    return metrics, cm, (fpr, tpr, thresholds)

