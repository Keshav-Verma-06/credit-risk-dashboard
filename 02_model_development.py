def evaluate_model(model, X_test, y_test, model_name):
    """
    Evaluate model on test set and return key metrics.
    Focus: Recall for Bad class (label=1), ROC-AUC, Accuracy
    """
    y_pred = model.predict(X_test)
    y_pred_proba = model.predict_proba(X_test)[:, 1] if hasattr(model, "predict_proba") else None

    metrics = {
        'accuracy': accuracy_score(y_test, y_pred),
        'recall_bad': recall_score(y_test, y_pred, pos_label=1),  # CRITICAL METRIC
        'roc_auc': roc_auc_score(y_test, y_pred_proba) if y_pred_proba is not None else np.nan
    }

    print(f"\n📈 {model_name} - Test Set Performance:")
    print(f"   • Accuracy:  {metrics['accuracy']:.4f}")
    print(f"   • Recall@Bad: {metrics['recall_bad']:.4f}  ← PRIMARY TARGET")
    print(f"   • ROC-AUC:   {metrics['roc_auc']:.4f}")

    # Optional: Print confusion matrix for business interpretation
    cm = confusion_matrix(y_test, y_pred)
    print(f"\n🧮 Confusion Matrix (Actual\\Predicted):")
    print(f"           Pred=Good  Pred=Bad")
    print(f"Actual=Good    {cm[0,0]:4d}      {cm[0,1]:4d}")
    print(f"Actual=Bad     {cm[1,0]:4d}      {cm[1,1]:4d}")
    print(f"\n💡 Interpretation: {cm[1,1]} defaults correctly caught out of {cm[1,0]+cm[1,1]} total defaults")

    return metrics

def log_to_mlflow(model, params, metrics, X_sample, model_name, run_name=None):
    """
    Log model, parameters, metrics, and artifact to MLflow.
    FINAL FIX: Use ONLY 'name' parameter (no artifact_path in newer MLflow)
    """
    with mlflow.start_run(run_name=run_name or model_name, nested=False):
        # Log parameters
        for key, value in params.items():
            mlflow.log_param(key, value)

        # Log metrics
        for key, value in metrics.items():
            mlflow.log_metric(key, value)

        # Log model artifact with signature for deployment
        # FIX: Use ONLY 'name' parameter (artifact_path is deprecated in newer MLflow)
        signature = infer_signature(X_sample, model.predict(X_sample))

        mlflow.sklearn.log_model(
            sk_model=model,
            name=model_name,  # ONLY this parameter - no artifact_path!
            signature=signature,
            input_example=X_sample.iloc[:1]  # Small example for schema inference
        )

        # Log additional context
        mlflow.log_param("random_state", RANDOM_STATE)
        mlflow.log_param("scoring_metric", "recall")
        mlflow.log_param("cv_strategy", "StratifiedKFold(n_splits=5)")

        print(f"✅ Logged {model_name} to MLflow run: {mlflow.active_run().info.run_id}")

        return mlflow.active_run().info.run_id

