"""## ⚙️ Feature Engineering - Creating Business-Value Features

### New Features & Business Justification:

| Feature | Formula/Logic | Business Rationale |
|---------|--------------|-------------------|
| `Credit_to_Duration` | Credit amount / Duration | **Monthly payment proxy**: Higher values indicate heavier monthly burden → higher default risk |
| `Age_Group` | Binned: 18-25, 26-35, 36-50, 50+ | **Life-stage risk segmentation**: Young adults lack history; seniors may have fixed income |
| `Liquidity_Flag` | 1 if Saving AND Checking are "high" else 0 | **Financial resilience indicator**: Applicants with strong liquidity buffers are lower risk |
| `High_Risk_Purpose` | 1 if purpose in ['radio/TV', 'repairs', 'furniture'] else 0 | **Discretionary spending flag**: Non-essential purchases correlate with higher default rates |

*These features transform raw data into actionable risk signals that models can leverage.*
"""

# ============================================================================
# CODE: Feature Engineering Implementation
# ============================================================================
df_engineered = df.copy()

# 1. Credit_to_Duration ratio (avoid division by zero)
df_engineered['Credit_to_Duration'] = df_engineered['Credit amount'] / (df_engineered['Duration'] + 1)
print("✅ Created: Credit_to_Duration (monthly payment proxy)")

# 2. Age_Group bins with business-relevant segments
age_bins = [17, 25, 35, 50, 100]
age_labels = ['18-25', '26-35', '36-50', '50+']
df_engineered['Age_Group'] = pd.cut(df_engineered['Age'], bins=age_bins, labels=age_labels, right=True)
print("✅ Created: Age_Group (life-stage segmentation)")

# 3. Liquidity_Flag: Strong financial buffer indicator
# Note: Adjust category names based on your dataset's actual values
high_saving = df_engineered['Saving accounts'].isin(['high', 'quite rich', 'rich']) if 'Saving accounts' in df_engineered.columns else False
high_checking = df_engineered['Checking account'].isin(['high', 'quite rich', 'rich']) if 'Checking account' in df_engineered.columns else False
df_engineered['Liquidity_Flag'] = ((high_saving) & (high_checking)).astype(int)
print("✅ Created: Liquidity_Flag (dual high-balance indicator)")

# 4. High_Risk_Purpose grouping (discretionary vs essential spending)
high_risk_purposes = ['radio/TV', 'repairs', 'furniture', 'domestic appliances']
df_engineered['High_Risk_Purpose'] = df_engineered['Purpose'].isin(high_risk_purposes).astype(int)
print("✅ Created: High_Risk_Purpose (discretionary spending flag)")

# Verify new features
print(f"\n📊 Engineered Features Added: {['Credit_to_Duration', 'Age_Group', 'Liquidity_Flag', 'High_Risk_Purpose']}")
display(df_engineered[['Credit_to_Duration', 'Age_Group', 'Liquidity_Flag', 'High_Risk_Purpose']].head())

# ============================================================================
# SECTION: Missing Value Treatment - Strategic Imputation
# ============================================================================
print("❗ Missing Values Before Imputation:")
print(df_engineered[['Saving accounts', 'Checking account']].isnull().sum())

# Impute missing categorical values with "Unknown" category
# BUSINESS JUSTIFICATION:
# - Missing ≠ zero balance; it means "no account history available"
# - "Unknown" preserves this signal for the model to learn from
# - Dropping rows would lose valuable applicants and introduce selection bias
# - Mode imputation would misrepresent unknown history as "typical" behavior

for col in ['Saving accounts', 'Checking account']:
    if df_engineered[col].isnull().any():
        df_engineered[col] = df_engineered[col].fillna('Unknown')
        print(f"✅ Imputed missing values in '{col}' with 'Unknown' category")

print("\n✅ Missing Values After Imputation:")
print(df_engineered[['Saving accounts', 'Checking account']].isnull().sum())

# Verify no remaining missing values in critical columns
assert df_engineered[['Saving accounts', 'Checking account', 'Risk']].isnull().sum().sum() == 0, "❗ Remaining missing values detected!"
print("\n🎯 Validation: All critical features now complete")

# ============================================================================
# SECTION: Target Encoding - Prepare for Classification
# ============================================================================
# Convert target to numeric: Good=0 (majority/no event), Bad=1 (minority/event)
# WHY 1 = Default?
# - Aligns with standard binary classification convention (positive class = event of interest)
# - Enables direct interpretation of model probabilities as "default probability"
# - Simplifies evaluation: precision/recall focus on identifying Bad risks

target_mapping = {'Good': 0, 'Bad': 1}
df_engineered['Risk'] = df_engineered['Risk'].map(target_mapping)

print("✅ Target variable encoded: Good → 0, Bad → 1")
print(f"📊 Updated distribution:\n{df_engineered['Risk'].value_counts(normalize=True)*100}")

# ============================================================================
# SECTION: Stratified Train-Test Split - Preserve Class Distribution
# ============================================================================
# Define features and target
feature_cols = [col for col in df_engineered.columns if col not in ['Risk']]
X = df_engineered[feature_cols].copy()
y = df_engineered['Risk'].copy()

# Stratified split: CRITICAL for imbalanced credit risk data
# WHY STRATIFY?
# - Ensures both train/test sets have same % of Good/Bad cases
# - Prevents scenarios where test set has no Bad examples (invalid evaluation)
# - Maintains statistical representativeness for reliable performance estimates

X_train, X_test, y_train, y_test = train_test_split(
    X, y,
    test_size=0.2,
    stratify=y,  # PRESERVE CLASS RATIOS
    random_state=RANDOM_STATE,
    shuffle=True
)

print(f"✅ Stratified split completed:")
print(f"   • Training set: {X_train.shape[0]} samples ({len(X_train)/len(X)*100:.1f}%)")
print(f"   • Test set: {X_test.shape[0]} samples ({len(X_test)/len(X)*100:.1f}%)")
print(f"   • Train class distribution: {dict(y_train.value_counts(normalize=True)*100)}")
print(f"   • Test class distribution: {dict(y_test.value_counts(normalize=True)*100)}")

